    public KhuyenMai getModel() {
        KhuyenMai km = new KhuyenMai();
        km.setMaKhuyenMai(txtMaKM.getText());
        km.setTenKhuyenMai(txtTenKM.getText());
        km.setDieukien(Integer.parseInt(txtDK.getText()));
        km.setPhantramgiam(Integer.parseInt(txtGG.getText()));
        try {
            km.setNgayBatDau(formatter.parse(txtNgayBD.getText()));
            km.setNgayKetThuc(formatter.parse(txtNgayKT.getText()));
        } catch (Exception e) {
        }

        return km;
    }

    private boolean check() {
        if (txtMaKM.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Mã khuyến mãi");
            return false;
        }
        for (KhuyenMai km : list) {
            if (km.getMaKhuyenMai().equalsIgnoreCase(txtMaKM.getText())) {
                JOptionPane.showMessageDialog(this, "Mã Khuyến mãi đã tồn tại");
                return false;
            }
        }
        if (txtTenKM.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Tên khuyến mãi");
            return false;
        }
        return true;
    }

   
