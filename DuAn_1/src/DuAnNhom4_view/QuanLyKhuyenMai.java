    private void showDetail() {
        KhuyenMai km = list.get(index);
        txtMaKM.setText(km.getMaKhuyenMai());
        txtTenKM.setText(km.getTenKhuyenMai());
        txtNgayBD.setText(formatter.format(km.getNgayBatDau()));
        txtNgayKT.setText(formatter.format(km.getNgayKetThuc()));
        txtGG.setText(String.valueOf(km.getPhantramgiam()));
        txtDK.setText(String.valueOf(km.getDieukien()));

    }

    private void fillToTable() {

        list = KhuyenMai_repository.getAllKM();
        mol = (DefaultTableModel) tblKhuyenMai.getModel();
        mol.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            Date now = new Date();
            Object[] data = new Object[]{list.get(i).getMaKhuyenMai(), list.get(i).getTenKhuyenMai(), list.get(i).getNgayBatDau(), list.get(i).getNgayKetThuc(), list.get(i).getPhantramgiam(),
                list.get(i).getDieukien()};
            mol.addRow(data);
        }
    }

    private void loadDataTblKhuyenMai() {
        list = KhuyenMai_repository.getAllKM();
        mol = (DefaultTableModel) tblKhuyenMai.getModel();
        mol.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        DecimalFormat dcf = new DecimalFormat(">###,###");
        for (KhuyenMai gg : list) {
            Vector vec = new Vector();
            vec.add(gg.getMaKhuyenMai());
            vec.add(gg.getTenKhuyenMai());
            vec.add(sdf.format(gg.getNgayBatDau()));
            vec.add(sdf.format(gg.getNgayKetThuc()));
            vec.add(gg.getPhantramgiam());
            vec.add(dcf.format(gg.getDieukien()));
            Date now = new Date();
            if (gg.getNgayBatDau().before(now) && gg.getNgayKetThuc().after(now)) {
                vec.add("Có hiệu lực");
            } else {
                vec.add("Không hiệu lực");
            }

            mol.addRow(vec);
        }
    }

