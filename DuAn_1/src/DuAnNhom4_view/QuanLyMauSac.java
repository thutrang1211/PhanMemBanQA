   private void fillToTable() {
        try {
            list = MauSac_repository.getAll();
            mol = (DefaultTableModel) tblLoaiSP.getModel();
            mol.setRowCount(0);
            for (int i = 0; i < list.size(); i++) {
                Object[] data = new Object[]{list.get(i).getID(), list.get(i).getMAMS(), list.get(i).getTENMAU()};
                mol.addRow(data);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            //  Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void showDetail() {
        MauSac lsp = list.get(index);
        txtMa.setText(lsp.getMAMS());
        txtTen.setText(lsp.getTENMAU());
    }

    public MauSac getModel() {
        MauSac lsp = new MauSac();
        lsp.setMAMS(txtMa.getText());
        lsp.setTENMAU(txtTen.getText());
        return lsp;
    }

    private void clearForm() {
        txtMa.setText("");
        txtTen.setText("");
    }

    public boolean check() {
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Mã ");
            return false;
        }
        if (txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên");
            return false;
        }
        return true;
    }
