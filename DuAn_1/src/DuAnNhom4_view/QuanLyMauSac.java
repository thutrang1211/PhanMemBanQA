    private void btnThemLoaiSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLoaiSPActionPerformed
                MauSac lsp = getModel();
                try {
                        if (check()) {
                                MauSac_repository.them(lsp);
                                fillToTable();
                                index = list.size() - 1;
                                JOptionPane.showMessageDialog(this, "Thêm thành công");
                            }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "lỗi Thêm ");
                        return;
                    }
    }//GEN-LAST:event_btnThemLoaiSPActionPerformed

    private void btnSuaLoaiSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaLoaiSPActionPerformed
                MauSac lsp = getModel();
                try {
                        if (list.isEmpty()) {
                                JOptionPane.showMessageDialog(this, "List ko còn dữ liệu để sửa");
                                return;
                            }
                        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không");
                        if (hoi != JOptionPane.YES_OPTION) {
                                return;
                            }
                        MauSac_repository.sua(lsp);
                        fillToTable();
                        index = list.size() - 1;
                        JOptionPane.showMessageDialog(this, "Sửa thanh cong");
            
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "lỗi Sửa");
                        return;
                    }
    }//GEN-LAST:event_btnSuaLoaiSPActionPerformed

